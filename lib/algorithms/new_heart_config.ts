//
// الأمان: حتى في 'apply' لا يلمس إلا تحسينات الدليقيتر كمبادلة دورين، وكلّ شيءٍ قابلٌ
// للعودة بإعادة بناء الأسبوع. لو ظهر خطأٌ: غيّر mode إلى 'shadow' أو 'off'.
// ═══════════════════════════════════════════════════════════════
